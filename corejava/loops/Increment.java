package loops;

public class Increment {

	public static void main(String[] args) {
		int a;
	      a = 2; 
	      System.out.println( a );  
	      System.out.println( a++ ); 
	      System.out.println( a );  
	      System.out.println();     
	     a = 11; 
	      System.out.println( a ); 
	      System.out.println( ++a ); 
	      System.out.println( a );   
		

	}

}
